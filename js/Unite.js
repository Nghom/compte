function unite(a) {
    if (a == 0) {
      return "Zéro";
    } else if (a == 1) {
      return "Un";
    } else if (a == 2) {
      return "Deux";
    } else if (a == 3) {
      return "Trois";
    } else if (a == 4) {
      return "Quatre";
    } else if (a == 5) {
      return "Cinq";
    } else if (a == 6) {
      return "Six";
    } else if (a == 7) {
      return "Sept";
    } else if (a == 8) {
      return "huit";
    } else if (a == 9) {
      return "Neuf";
    }
}
